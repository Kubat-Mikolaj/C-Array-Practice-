using namespace std;

int main()
{
    int arr[10];
    int i, num;

    do
    {
        cout << "Input the number of elements in Array (max 10): \n";
        cin >> num;
    } while (num <= 0);
    
    for (i = 0; i < num; i++)
    {
        cout << "Enter element " << i + OFFSET_1 << " : ";
        cin >> arr[i];
    }

    cout << "\nThe Array: ";
    for (i = 0; i < num; i++)
    {   
        cout << "\n";
        cout << "Element " << i + OFFSET_1 << " is " << arr[i] << " ";
    }
    cout << endl;
}